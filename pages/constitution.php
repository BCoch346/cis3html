
<h2>CISSS Constitution</h2>
<h3>Article I</h3>
<h4>Name</h4>
<b>Full name of the club with acronym in brackets:</b>

Computer Information Systems Students’ Society (CISSS) or (CIS3)

<hr />

<h3>Article II</h3>
<h4>Objectives</h4>
<b>This is the purpose of forming the club.</b>

The objectives of CISSS are as follows:
<ol>
	<li>To provide peer support and professional development opportunities to supplement academic pursuits (i.e. events, networking, team building, industry site visits).</li>
	<li>To disseminate leads on volunteer and paid job opportunities for gaining industry experience</li>
	<li>To connect the students and professors through a collaborative and open network (Facebook)</li>
	<li>To promote awareness of the CIS program within the MRU community.</li>
	<li>To present a professional, presentable public face for members of the club, the BCIS degree, and the developmental interests thereof.</li>
</ol>

<hr />

<h3>Article III</h3>
<h4>Membership</h4>
<b>This states who is eligible for membership and the definition of an active member. Will the club charge membership fees and if so, how much?</b>
<ul>
	<li>All students are welcome to be members, regardless of whether they are a student in the CIS program or not</li>
	<li>Membership dues are $0 per academic year</li>
	<li>Members are not required to attend meetings unless they are an Executive Officer</li>
	<li>All Executive Officers are considered active members</li>
	<li>All club representatives are considered active members</li>
	<li>All of those who have expressed interest in participating and helping with club activities are considered active members</li>
	<li>All of those who have joined the Facebook group or who have signed up to the mailing list are considered active members</li>
</ul>

