<h3>Article X</h3>
<h4>Property</h4>
<b>State the procedures for purchases, use, and disposal of club property.</b>

Purchases of club property must first be approved by the treasurer, then by the vice-president, and then the president.

Resources, materials, articles and devices donated to the club become club property in perpetuity, unless specific agreements with the donor are produced in written form as to the term of release for the above elements. Resources which are donated ‘on loan’ become the property of the original donor or their designate at the culmination of the term, and no resources, permanent or temporary, may become property of a club member or executive unless they are the original donor.

Articles and resources which have outlived their useful life expectancy (1 full term before consideration) may be proposed by the club executive through the treasurer for liquidation sale, to which club members are given a priority option to bid on the article. While baseline starting bids and bottom lines may be generated for these articles, a public window of auction must be made available through all channels to active club members before liquidation of a club resource deemed out of date may take place. No fixed point of upper value may be established, and as such auctioned items cannot simply be ‘bought out’ before the auction window is closed. All active members are eligible to bid on legal liquidations, including alumni.

Any club-branded articles, such as hoodies, promotional materials, or other equivalent articles developed through direct club fundraising or budgeted moneys must be properly documented with the club as to purchase price, sale price, margin and profitability. Acquisition and sale of these articles should be performed through the treasurer, and a catalogue of available branded material should be retained for reference.

Use and disposal of club property must be a unanimous decision between all executive officers.

