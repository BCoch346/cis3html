<h3>Article VI</h3>
<h4>Meetings</h4>
<b>State the frequency of meetings, the number of members required for quorum, and the method of publication.</b>

Meetings shall be held once per month. An appropriate date in the month will be determined once class schedules have been determined. Meetings are to be held as early in the month as possible. At least two of the Executive Officers must be present to reach quorum. Meeting minutes will be gathered by at least one member. Minutes then are made available to the general membership via electronic means (i.e. website or email).

<hr />

<h3>Article VII</h3>
<h4>Impeachment</h4>
<b>State the procedures and majority vote required to impeach an Executive Officer and / or a member.</b>

A member who knowingly violates any provisions of the Constitution or acts in a manner that puts the CISSS in disrepute may be subject to expulsion. The erring member shall be duly informed of their offence and be allowed proper self-defence through oral or written submissions.

Members who fail to perform the duties of their executive roles, consistently evade required meetings, or simply do not contribute to their position may be asked to step down from said position. Failure to negotiate an inactive executive’s status may remit to Article VIII, and successful dissolution of that executive position may result in an appointment or by-election, performed by standing executive members.

Any member can be impeached if 6 or more of the active membership/ Executive Officers give written request to a single member who will serve as the impeachment sponsor.

