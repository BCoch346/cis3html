<h3>Article V</h3>
<h4>Elections</h4>
<b>Outline the election procedures including dates and what constitutes quorum for an election to be valid.</b>
<ul>
	<li>Because of the nature of our club and short supply of active members and those who wish to be an executive, an unconventional approach to elections is taken</li>
	<li>If only one person is running for an executive position, that individual will secure the position by default</li>
	<li>If more than one person is running for the same position, an electoral board consisting of at least 3 individuals for integrity purposes will be created to coordinate the voting process for the individuals running for the same position. This board may consist of executive officers and active members. This board will provide an opportunity for active members to vote</li>
	<li>Elections for the executive officers for the following calendar year shall be held during the second week of</li>
</ul>
October.

